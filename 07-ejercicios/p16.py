# 16. Programa en el cual se ingresen 2 números y luego realice las siguientes operaciones: 
#     a) Si los números son iguales restarlos b) Si los números son diferentes sumarlos 

num1=float(input('Ingresa el primer numero: '));  num2=float(input('Ingresa el segundo numero: '))
if num1==num2:
    print("Valores restados: ",num1-num2)
else:
    print("Valores sumados: ",num1+num2)
    