# 3. Programa para calcular la distancia recorrida en un movimiento rectilíneo. Recuerde que X = V*T
# ¿Que distancia recorrio un auto en 30 segundos si llevaba una velocidad constante de 10 metros / segundos?

velocidad = 10 #m/s
tiempo = 30 #seconds

distanciaRecorida = velocidad * tiempo
print("la distancia recorida del auto fue de: ", distanciaRecorida ," metros")